function cellInfo = PropCmdBus(varargin)
% PROPACTUATORBUS returns a cell array containing bus object information 
% 
% Optional Input: 'false' will suppress a call to Simulink.Bus.cellToObject 
%                 when the MATLAB file is executed. 
% The order of bus element attributes is as follows:
%   ElementName, Dimensions, DataType, SampleTime, Complexity, SamplingMode, DimensionsMode, Min, Max, DocUnits, Description 

suppressObject = false;
numEng=1;
if nargin >= 1
    if islogical(varargin{1}) && varargin{1} == false 
      suppressObject = true;
    end
    if nargin == 2
        numEng=varargin{2};
    end
end 

cellInfo = { ... 
  { ... 
    'BUS_PROP_CMD', ... 
    'BusPropCmd.h', ... 
    '', ... 
    'Exported', ... 
    '-1', {... 
{'EngCmd', numEng, 'double', -1, 'real', 'Sample', 'Fixed', [], [], '', ''}; ...
{'EngSpeed', 1, 'Bus: BUS_ENG_ACT', -1, 'real', 'Sample', 'Fixed', [], [], '', ''}; ...
{'EngSpeedSensed', 1, 'Bus: BUS_ENG_ACT', -1, 'real', 'Sample', 'Fixed', [], [], '', ''}; ...
    } ...
  } ...
  { ... 
    'BUS_ENG_ACT', ... 
    'BusEndAct.h', ... 
    '', ... 
    'Exported', ... 
    '-1', {... 
{'EngSpeed', numEng, 'double', -1, 'real', 'Sample', 'Fixed', [], [], '', ''}; ...
{'EngAccel', numEng, 'double', -1, 'real', 'Sample', 'Fixed', [], [], '', ''}; ...
    } ...
  } ...
}'; 

if ~suppressObject 
    % Create bus objects in the MATLAB base workspace 
    Simulink.Bus.cellToObject(cellInfo);
end 
